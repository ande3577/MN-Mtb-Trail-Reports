							<a name="Closed"></a><span ><font size="+1">Why Are Trails Closed?</font></span><br></font><br>
							
							Sustainable trails are built so that water will run 
							<strong>across</strong> the trail, <strong>not down it</strong>.  When water runs down the trail it focuses the water and increases erosion.  Riding wet trails creates ruts that will focus the water and increase erosion.
 
							<br><br>
							Riding wet trails also increases cupping of the trail surface and causes puddles to get deeper and wider, both of which slow the recovery of the trail.<br><br>
 
Trails are closed (and often fenced off) during the freeze/thaw cycle in the fall and spring.  During these times, trails are especially susceptible to significant damage.<br><br>
 
Trails may also be closed after significant weather events, or if there are unsafe trail conditions.<br><br>
 
The trails around the Twin Cities metro area are particularly sensitive due to the very large numbers of users these trails get every year.<br><br>
 
When trails turn into maintenance headaches, land managers start to consider closing trails for good... PLEASE do your part to ensure that we have access to great trails by riding responsibly.  Together we can keep these mountain biking opportunities available for generations to come!
